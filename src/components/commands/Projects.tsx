import { useContext, useEffect } from "react";
import {
  checkRedirect,
  getCurrentCmdArry,
  isArgInvalid,
} from "../../utils/funcs";
import {
  ProjectContainer,
  ProjectDesc,
  ProjectsIntro,
  ProjectTitle,
} from "../styles/Projects.styled";
import { termContext } from "../Terminal";
import Usage from "../Usage";
import { portfolioConfig } from "../../config/portfolio.config";

const Projects: React.FC = () => {
  // ðŸŽ¯ TEMPLATE CUSTOMIZATION: Edit projects in src/config/portfolio.config.ts
  const { arg, history, rerender } = useContext(termContext);
  const { projects } = portfolioConfig;

  /* ===== get current command ===== */
  const currentCommand = getCurrentCmdArry(history);

  /* ===== check current command is redirect ===== */
  useEffect(() => {
    if (checkRedirect(rerender, currentCommand, "projects")) {
      projects.forEach(({ id, url }) => {
        id === parseInt(arg[1]) && window.open(url, "_blank");
      });
    }
  }, [arg, rerender, currentCommand]);

  /* ===== check arg is valid ===== */
  const validProjectIds = projects.map(p => p.id.toString());
  const checkArg = () =>
    isArgInvalid(arg, "go", validProjectIds) ? (
      <Usage cmd="projects" />
    ) : null;

  return arg.length > 0 || arg.length > 2 ? (
    checkArg()
  ) : (
    <div data-testid="projects">
      <ProjectsIntro>
        "Talk is cheap. Show me the code"? I got you. <br />
        Here are some learning projects as I develop my fullstack skills
      </ProjectsIntro>
      {projects.map(({ id, title, desc }) => (
        <ProjectContainer key={id}>
          <ProjectTitle>{`${id}. ${title}`}</ProjectTitle>
          <ProjectDesc>{desc}</ProjectDesc>
        </ProjectContainer>
      ))}
      <Usage cmd="projects" marginY />
    </div>
  );
};

// Projects are now configured in src/config/portfolio.config.ts
// This makes it easy for template users to customize their projects

export default Projects;
